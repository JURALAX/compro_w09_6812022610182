// การใช้ for loop
#include <stdio.h>
int main(){
    int i ; // กำหนดค่า i
   
    for ( i = 1; i <= 10; i++) //กำหนดค่าเริ่มต้น เป็น 1 , i มีค่าน้อยกว่าหรือเท่ากับ , เพิ่มค่า
    {
       if (i == 6)//เงื่อนไข i เมื่อ i = 6 คำสั่ง continue จะทำการข้ามตัวเลข ตัวนั้นๆ
       {
        continue; // คำสั่งข้าม
       }
       
      printf("%d " , i); //แสดงค่าของ i
       
    }
    return 0 ;
}